    std::string extension_to_mime(std::string const& extension)
    {
        auto iter = extension_mime_mapping.find(extension);
        if (iter == std::end(extension_mime_mapping))
            return "";
        else
            return iter->second;
    }
//---------------------------------------------------------------------------------------------------------------------
    std::string search_mime(std::string const& part)
    {
        for (auto const& i : extension_mime_mapping)
        {
            if (i.second.find(part) != std::string::npos)
                return i.second;
        }
        return "";
    }
